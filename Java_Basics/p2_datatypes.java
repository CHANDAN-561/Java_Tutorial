class p2_datatypes {
    public static void main (String[] args) {
        byte by = 24;  //8bit int (-128 to 127)
        System.out.println("Byte type: "+ by);

        short sh = 5;  //16bit int 
        System.out.println("Short type: "+ sh);

        float fl = 83.66f; //32bit floating point
        System.out.println("Float type: "+ fl);

        double dl = 235.7; //64bit floating point
        System.out.println("Double type: "+ dl);

        String name = "Chandan";
        System.out.println("String type: "+ name);

        int age = 20; //32bit int
        System.out.println("Int type: "+ age);

        char ch = 'A'; //16bit unicode charcter
        System.out.println("Char type: "+ ch);

        boolean gender = false;  //true = F, false = M
        System.out.println("Boolean type: "+ gender);

        long ln = 558848;
        System.out.println("Long type: "+ ln);

    }
}